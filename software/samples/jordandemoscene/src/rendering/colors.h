#ifndef COLORS_H
#define COLORS_H

#include "config.h"

#if USE_16BIT_COLOR
// Uses 4 byte rgb (4 different vertical colors)
#define SOLIDRED      ((0xF00 << 16) | 0xF00)
#define SOLIDBLUE     ((0x00F << 16) | 0x00F)
#define SOLIDGREEN    ((0x0F0 << 16) | 0x0F0)
#define SOLIDYELLOW   ((0xFF0 << 16) | 0xFF0)
#define SOLIDCYAN     ((0x0FF << 16) | 0x0FF)
#define SOLIDMAGENTA  ((0xF0F << 16) | 0xF0F)
#define SOLIDWHITE    ((0xFFF << 16) | 0xFFF)
#define SOLIDBLACK    ((0x000 << 16) | 0x000)
#define SOLIDORANGE   ((0xFF7 << 16) | 0xFF7)
#define SOLIDPURPLE   ((0xF0F << 16) | 0xF0F)

#else
// Uses RGB565 (2 different vertical colors)
#define SOLIDRED      ((CONSOLERED << 24) | (CONSOLERED << 16) | (CONSOLERED << 8) | CONSOLERED)
#define SOLIDBLUE     ((CONSOLEBLUE << 24) | (CONSOLEBLUE << 16) | (CONSOLEBLUE << 8) | CONSOLEBLUE)
#define SOLIDGREEN    ((CONSOLEGREEN << 24) | (CONSOLEGREEN << 16) | (CONSOLEGREEN << 8) | CONSOLEGREEN)
#define SOLIDYELLOW   ((CONSOLEYELLOW << 24) | (CONSOLEYELLOW << 16) | (CONSOLEYELLOW << 8) | CONSOLEYELLOW)
#define SOLIDCYAN     ((CONSOLECYAN << 24) | (CONSOLECYAN << 16) | (CONSOLECYAN << 8) | CONSOLECYAN)
#define SOLIDMAGENTA  ((CONSOLEMAGENTA << 24) | (CONSOLEMAGENTA << 16) | (CONSOLEMAGENTA << 8) | CONSOLEMAGENTA)
#define SOLIDWHITE    ((CONSOLEWHITE << 24) | (CONSOLEWHITE << 16) | (CONSOLEWHITE << 8) | CONSOLEWHITE)
#define SOLIDBLACK    ((CONSOLEDIMGRAY << 24) | (CONSOLEDIMGRAY << 16) | (CONSOLEDIMGRAY << 8) | CONSOLEDIMGRAY)
#define SOLIDORANGE   ((CONSOLEYELLOW << 24) | (CONSOLEYELLOW << 16) | (CONSOLEYELLOW << 8) | CONSOLERED)
#define SOLIDPURPLE   ((CONSOLEMAGENTA << 24) | (CONSOLEMAGENTA << 16) | (CONSOLEMAGENTA << 8) | CONSOLERED)

#endif

#endif // COLORS_H
